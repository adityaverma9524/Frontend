/* Type Coersion */

console.log(5 + 5);

let sum = '5' + 5;
console.log(sum); // o/p => 55
console.log(typeof sum); // o/p => string

let fullName = "Aditya" + " " + "Verma";
console.log(fullName); // o/p => Aditya Verma
console.log(typeof fullName); // o/p => string

let sub = '55' - 10;
console.log(sub); // o/p => 45 
console.log(typeof sub); // o/p => number

let prod = 4*2;
console.log(prod); // o/p => 8 
console.log(typeof prod); // o/p => number